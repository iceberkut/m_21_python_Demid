az = ['.-','-...','-.-.','-..','.','..-.','--.','....','..','.---','-.-',
'.-..','--','-.','---','.--.','--.-','.-.','...','-','..-','...-',
'.--','-..-','-.--','--..']
numbs = ['-----','.----','..---','...--','....-','....','-...','--..',
'---.','----']

text=input()
text = text.lower()

new_text = ''
for n,i in enumerate(list(text)):
    if 97 <= ord(i) <= 97 + 26: new_text+=az[ord(i)-97]+' '
    if 48 <= ord(i) <= 48 + 10: new_text+=numbs[ord(i)-48]+' '
    if ord(i) == 32: new_text += '\n'

print(new_text)